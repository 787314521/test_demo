      <div class="word">17、参与分配的风险——比例受偿或债权不能全部实现。</div>
      <div class="word">《最高人民法院关于适用《中华人民共和国民事诉讼法》的解释》第五百一十条参与分配执行中，执行所得价款扣除执行费用，并清偿应当优先受偿的债权后，对于普通债权，原则上按照其占全部申请参与分配债权数额的比例受偿。清偿后的剩余债务，被执行人应当继续清偿。债权人发现被执行人有其他财产的，可以随时请求人民法院执行。</div>
      <div class="word">18、执行回转的风险——责令取得财产的人返还；拒不返还的，强制执行。</div>
      <div class="word">《中华人民共和国民事诉讼法》第二百三十三条执行完毕后，据以执行的判决、裁定和其他法律文书确有错误，被人民法院撤销的，对已被执行的财产，人民法院应当作出裁定，责令取得财产的人返还；拒不返还的，强制执行。</div>
      <div class="word">19、执行转破产的风险——比例受偿或债权不能全部实现。</div>
      <div class="word">《最高人民法院关于人民法院执行工作若干问题的规定(试行)》89．被执行人为企业法人，其财产不足清偿全部债务的，可告知当事人依法申请被执行人破产。</div>
      <div class="word">《最高人民法院关于适用《中华人民共和国民事诉讼法》的解释》第五百一十三条在执行中，作为被执行人的企业法人符合企业破产法第二条第一款规定情形的，执行法院经申请执行人之一或者被执行人同意，应当裁定中止对该被执行人的执行，将执行案件相关材料移送被执行人住所地人民法院。</div>
      <div class="word">《中华人民共和国民事诉讼法》第五百一十四条被执行人住所地人民法院应当自收到执行案件相关材料之日起三十日内，将是否受理破产案件的裁定告知执行法院。不予受理的，应当将相关案件材料退回执行法院。</div>
      <div class="word">第五百一十五条被执行人住所地人民法院裁定受理破产案件的，执行法院应当解除对被执行人财产的保全措施。被执行人住所地人民法院裁定宣告被执行人破产的，执行法院应当裁定终结对该被执行人的执行。</div>
      <div class="word">被执行人住所地人民法院不受理破产案件的，执行法院应当恢复执行。</div>
      <div class="word">第五百一十六条当事人不同意移送破产或者被执行人住所地人民法院不受理破产案件的，执行法院就执行变价所得财产，在扣除执行费用及清偿优先受偿的债权后，对于普通债权，按照财产保全和执行中查封、扣押、冻结财产的先后顺序清偿。</div>
      <div class="word">20、诉讼保全的风险——保全错误造成执行不能风险。</div>
      <div class="word">《中华人民共和国民事诉讼法》第一百条人民法院对于可能因当事人一方的行为或者其他原因，使判决难以执行或者造成当事人其他损害的案件，根据对方当事人的申请，可以裁定对其财产进行保全、责令其作出一定行为或者禁止其作出一定行为；当事人没有提出申请的，人民法院在必要时也可以裁定采取保全措施。</div>
      <div class="word">人民法院采取保全措施，可以责令申请人提供担保，申请人不提供担保的，裁定驳回申请。</div>
      <div class="word">人民法院接受申请后，对情况紧急的，必须在四十八小时内作出裁定；裁定采取保全措施的，应当立即开始执行。</div>
      <div class="word">第一百零一条利害关系人因情况紧急，不立即申请保全将会使其合法权益受到难以弥补的损害的，可以在提起诉讼或者申请仲裁前向被保全财产所在地、被申请人住所地或者对案件有管辖权的人民法院申请采取保全措施。申请人应当提供担保，不提供担保的，裁定驳回申请。</div>
      <div class="word">人民法院接受申请后，必须在四十八小时内作出裁定；裁定采取保全措施的，应当立即开始执行。</div>
      <div class="word">申请人在人民法院采取保全措施后三十日内不依法提起诉讼或者申请仲裁的，人民法院应当解除保全。</div>
      <div class="word">第一百零五条申请有错误的，申请人应当赔偿被申请人因保全所遭受的损失。</div>
      <div class="word">21、先予执行的风险——驳回申请或赔偿被申请人因先予执行遭受的财产损失。</div>
      <div class="word">《中华人民共和国民事诉讼法》第一百零六条人民法院对下列案件，根据当事人的申请，可以裁定先予执行：</div>
      <div class="word">（一）追索赡养费、扶养费、抚育费、抚恤金、医疗费用的；</div>
      <div class="word">（二）追索劳动报酬的；</div>
      <div class="word">（三）因情况紧急需要先予执行的。</div>
      <div class="word">第一百零七条人民法院裁定先予执行的，应当符合下列条件：</div>
      <div class="word">（一）当事人之间权利义务关系明确，不先予执行将严重影响申请人的生活或者生产经营的；</div>
      <div class="word">（二）被申请人有履行能力。</div>
      <div class="word">人民法院可以责令申请人提供担保，申请人不提供担保的，驳回申请。申请人败诉的，应当赔偿被申请人因先予执行遭受的财产损失。</div>
