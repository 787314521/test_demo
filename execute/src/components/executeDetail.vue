      <div class="word">（三）作为被执行人的公民死亡，无遗产可供执行，又无义务承担人的；</div>
      <div class="word">（四）追索赡养费、扶养费、抚育费案件的权利人死亡的；</div>
      <div class="word">（五）作为被执行人的公民因生活困难无力偿还借款，无收入来源，又丧失劳动能力的；</div>
